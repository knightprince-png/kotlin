fun main() {
    //arithmetic operator
    var x=20
    var y=5
    println(x+y)
    println(x*y)
    println(x-y)
    println(x/y)
    println(x%y) //modulers

    //comparison operators
    var a=67
    var v=30
    println(a > v)
    println(a < v)
    println(a >= v)
    println(a <= v)
    println(a == v)
    println(a != v)


    //assignment operators
    var number=90
    println(number)

    number-=3
    println(number)

    //logical operators -&&,||,!
    var num=10
    println(num > 67 && num < 20)
    println(num > 67 || num < 20)
    println(!(num > 67 || num < 20))




}